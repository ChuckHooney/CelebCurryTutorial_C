#include<stdio.h>
int main()
{
        int x = 1;
        printf("%i\n", x);

        x += 9;
        printf("%i\n", x);

        x -= 5;
        printf("%i\n", x);

        x *= 3;
        printf("%i\n", x);

        x++;
        printf("%i\n", x);

        --x;
        printf("%i\n", x);

        --x;
        printf("%i\n", x);

        x = 4;
        printf("%i\n", x);

        return 0;
}
